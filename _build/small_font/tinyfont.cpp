GLuint tinyfont::texture;


void tinyfont::createText(tinytext &intext, const char *inphrase, const tinycolor &incolor, const vec3f &inpos) {

    static GLfloat tex_left, tex_right, tex_top, tex_bottom;
    static GLfloat x_offset;

    intext.vec_vertex.clear();
    intext.vec_texture.clear();
    intext.vec_color.clear();

    x_offset = 0;

    for ( unsigned int i=0; i < strlen(inphrase); ++i ) {

        //atlas_x = inphrase[i]/16;
        //atlas_y = inphrase[i]%16;

        tex_left = 0.0625 * (inphrase[i]/16);
        tex_right = tex_left + 0.0625;
        tex_bottom = 0.0625 * (15-(inphrase[i]%16));
        tex_top = tex_bottom + 0.0625;

        //top left
        intext.vec_texture.push_back(tex_left);
        intext.vec_texture.push_back(tex_top);
        intext.vec_vertex.push_back(inpos.x+x_offset);
        intext.vec_vertex.push_back(inpos.y);
        intext.vec_vertex.push_back(inpos.z);

        //top right
        intext.vec_texture.push_back(tex_right);
        intext.vec_texture.push_back(tex_top);
        intext.vec_vertex.push_back(inpos.x+8+x_offset);
        intext.vec_vertex.push_back(inpos.y);
        intext.vec_vertex.push_back(inpos.z);

        //bottom left
        intext.vec_texture.push_back(tex_left);
        intext.vec_texture.push_back(tex_bottom);
        intext.vec_vertex.push_back(inpos.x+x_offset);
        intext.vec_vertex.push_back(inpos.y-8);
        intext.vec_vertex.push_back(inpos.z);

        //bottom right
        intext.vec_texture.push_back(tex_right);
        intext.vec_texture.push_back(tex_bottom);
        intext.vec_vertex.push_back(inpos.x+8+x_offset);
        intext.vec_vertex.push_back(inpos.y-8);
        intext.vec_vertex.push_back(inpos.z);

        x_offset += 8;

        for (int j=0; j<4; ++j) {
            intext.vec_color.push_back(incolor.r);
            intext.vec_color.push_back(incolor.g);
            intext.vec_color.push_back(incolor.b);
            intext.vec_color.push_back(incolor.a);
        }
    }

}


void tinyfont::beginRender() {
    glPushAttrib(GL_COLOR_BUFFER_BIT | GL_TEXTURE_BIT | GL_LIGHTING_BIT);
    glPushClientAttrib(GL_CLIENT_VERTEX_ARRAY_BIT);
    glEnable(GL_TEXTURE_2D);
    glEnable(GL_BLEND);
    glBlendFunc(GL_SRC_ALPHA,GL_ONE_MINUS_SRC_ALPHA);
    glDisable(GL_LIGHTING);
    glDisable(GL_DEPTH_TEST);
    glBindTexture(GL_TEXTURE_2D, texture);

    glEnableClientState(GL_VERTEX_ARRAY);
    glEnableClientState(GL_COLOR_ARRAY);
    glEnableClientState(GL_TEXTURE_COORD_ARRAY);
}


void tinyfont::endRender() {

    //glDisableClientState(GL_VERTEX_ARRAY);
    //glDisableClientState(GL_COLOR_ARRAY);
    //glDisableClientState(GL_TEXTURE_COORD_ARRAY);

    glPopClientAttrib();
    glPopAttrib();
}


void tinyfont::render(tinytext &in) {

    glVertexPointer(3, GL_FLOAT, 0, &in.vec_vertex[0] );
    glTexCoordPointer(2, GL_FLOAT, 0, &in.vec_texture[0] );
    glColorPointer(4, GL_FLOAT, 0, &in.vec_color[0] );
    glDrawArrays(GL_TRIANGLE_STRIP, 0, in.vec_vertex.size()/3 );

}



